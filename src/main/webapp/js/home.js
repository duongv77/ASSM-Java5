//menu điện thoại
var menuphoneElement = document.querySelector('#menu-phone')
var phoneElement = document.querySelector('#phone')
function over( element){
    element.classList = ''
}
function out( element){
    element.classList = 'closed'
}
phoneElement.onmouseover = ()=>{
    over(menuphoneElement)
}
phoneElement.onmouseout = ()=>{
    out(menuphoneElement);
}

menuphoneElement.onmouseover = ()=>{
    over(menuphoneElement)
}

menuphoneElement.onmouseout = ()=>{
    out(menuphoneElement);
}
